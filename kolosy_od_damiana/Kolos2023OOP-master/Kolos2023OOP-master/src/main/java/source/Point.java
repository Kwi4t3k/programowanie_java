package source;

public class Point {
    // Współrzędne punktu
    public final double x;
    public final double y;

    // Konstruktor klasy Point
    public Point(double x, double y) {
        this.x = x; // Przypisanie wartości do zmiennej x
        this.y = y; // Przypisanie wartości do zmiennej y
    }
}
